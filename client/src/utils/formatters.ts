// Amount formatting with color classes
export const formatAmountWithColor = (amount: number, type: 'income' | 'expense'): string => {
  const formatted = formatCurrency(amount);
  const colorClass = type === 'income' ? 'amount-positive' : 'amount-negative';
  return `<span class="${colorClass}">${formatted}</span>`;
};

// Category formatting
export const formatCategoryName = (name: string, icon?: string): string => {
  if (icon) {
    return `${icon} ${name}`;
  }
  return name;
};

// Transaction type formatting
export const formatTransactionType = (type: 'income' | 'expense'): string => {
  return type.charAt(0).toUpperCase() + type.slice(1);
};

// File size formatting
export const formatFileSize = (bytes: number): string => {
  if (bytes === 0) return '0 Bytes';
  
  const k = 1024;
  const sizes = ['Bytes', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  
  return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
};

